x = 5

# Use the += operator
x += 3  # This is equivalent to x = x + 3

# Print the updated value
print(x)  # Output will be 8

my_string = ""

# Concatenate using +=
my_string += "Hello"  # Now my_string is "Hello"
my_string += " "      # Now my_string is "Hello "
my_string += "World!" # Now my_string is "Hello World!"

# Print the final string
print(my_string)  # Output: Hello World!